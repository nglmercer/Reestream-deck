const KeyboardSelector = () => {
  const [key1, setKey1] = useState('');
  const [key2, setKey2] = useState('');

  const handlePrint = () => {
    console.log('Selector 1:', key1);
    console.log('Selector 2:', key2);
  };

  return (
    <div>
      <select value={key1} onChange={(e) => setKey1(e.target.value)}>
        <option value="">Select Key 1</option>
        {Object.entries(keyboard).map(([key, value]) => (
          <option key={key} value={value}>{key}</option>
        ))}
      </select>
      <select value={key2} onChange={(e) => setKey2(e.target.value)}>
        <option value="">Select Key 2</option>
        {Object.entries(valueboard).map(([key, value]) => (
          <option key={key} value={key}>{value}</option>
        ))}
      </select>
      <button onClick={handlePrint}>Print Selected Values</button>
    </div>
  );
};


